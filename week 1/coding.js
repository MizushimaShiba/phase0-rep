// FOR Loop

for (let i = 1; i <= 20; i++) {
  console.log(`${i} - I love coding`);
}

console.log(`================================================`);


for (let i = 20; i > 0; i--) {
  console.log(`${i} - I will become fullstack developer`);
}

console.log(`================================================`);

